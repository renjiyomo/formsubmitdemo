window.addEventListener('load', () => {
  const style = document.createElement('link');
  style.rel = 'preload';
  style.as = 'style';
  style.href = 'style.css';
  document.head.appendChild(style);

  const css = document.createElement('link');
  css.rel = 'stylesheet';
  css.href = 'style.css';
  document.head.appendChild(css);
});
